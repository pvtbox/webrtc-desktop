import os
import sys
import sipconfig

# The name of the SIP build file generated by SIP and used by the build
# system.
build_file = 'webrtc.sbf'

# Get the SIP configuration information.
config = sipconfig.Configuration()

# Run SIP to generate the code.
os.system(' '.join([config.sip_bin, '-c', '.', '-b', build_file, '-g', 'webrtc.sip']))

# Create the Makefile.
makefile = sipconfig.SIPModuleMakefile(config, build_file)

# Add the library we are wrapping.  The name doesn't include any platform
# specific prefixes or extensions (e.g. the "lib" prefix on UNIX, or the
# ".dll" extension on Windows).
makefile.extra_lib_dirs = ['.']

# Generate the Makefile itself.
makefile.generate()

with open('dependencies.txt', 'r') as f:
    dependencies = f.read()

makefile.LIBS._macro += dependencies.split(';')
makefile.LIBS._macro += ['-framework AppKit', '-framework Cocoa', '-framework IOKit', '-framework Security', '-framework SystemConfiguration', '-framework ApplicationServices', '-framework CoreServices', '-framework CoreVideo', '-framework CoreAudio', '-framework AudioToolbox', '-framework Carbon', '-framework Foundation', '-framework AVFoundation', '-framework QTKit', '-framework QuartzCore', '-framework CoreMedia', '-framework OpenGL', '-framework CoreVideo', '-framework CoreGraphics', '-framework CFNetwork', '-licucore']
makefile.LINK._macro = ['clang']

makefile.CXX._macro = ['clang']

makefile.CXXFLAGS._macro += ['-fno-rtti', '-std=c++11']

makefile.LFLAGS._macro += '-stdlib=libc++ -Wl,-dead_strip -Wl,-ObjC -Wcrl,strip,-x-S'.split(' ')

makefile.generate()

