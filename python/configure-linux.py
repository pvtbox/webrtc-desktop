import os
import sys
import sipconfig

# The name of the SIP build file generated by SIP and used by the build
# system.
build_file = 'webrtc.sbf'

# Get the SIP configuration information.
config = sipconfig.Configuration()

# Run SIP to generate the code.
os.system(' '.join([config.sip_bin, '-c', '.', '-b', build_file, '-g', 'webrtc.sip']))

# Create the Makefile.
makefile = sipconfig.SIPModuleMakefile(config, build_file)

# Add the library we are wrapping.  The name doesn't include any platform
# specific prefixes or extensions (e.g. the "lib" prefix on UNIX, or the
# ".dll" extension on Windows).
# Generate the Makefile itself.
makefile.generate()

with open('dependencies.txt', 'r') as f:
    dependencies = f.read()
makefile.LIBS._macro += ['-Wl,--start-group']
makefile.LIBS._macro += dependencies.split(';')
makefile.LIBS._macro += ['-Wl,--end-group']
makefile.LIBS._macro += ['-ldl', '-lpthread', '-lX11', '-lXcomposite', '-lXext', '-lXrender']

makefile.LINK._macro = ['g++']
makefile.CXX._macro = ['g++']

makefile.CXXFLAGS._macro += ['-fno-rtti', '-std=gnu++11', '-D_GLIBCXX_USE_CXX11_ABI=1']
makefile.generate()
